    private void btnCrearTipoPesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearTipoPesaActionPerformed
        try {
            String nombreArticulo = txtNombreArticulo.getText();
            String marcaArticulo = txtMarcaArticulo.getText();
            float costoArticulo = Float.parseFloat(txtCostoArticulo.getText());
            Amortizacion tipoDesgaste = (Amortizacion) selectTipoDesgaste.getSelectedItem();
            int desgasteMaximo = (Integer) spinnerDesgasteMaximo.getValue();

            // Asegurarse de que el costo no sea negativo
            if (costoArticulo <= 0) {
                JOptionPane.showMessageDialog(this, "El costo no puede ser negativo.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (desgasteMaximo <= 0) {
                JOptionPane.showMessageDialog(this, "El desgaste Maximo no puede ser negativo.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
                return;
            }

            float pesoPesa = Float.parseFloat(txtPesoPesa.getText());

            if (pesoPesa <= 0) {
                JOptionPane.showMessageDialog(this, "El peso no puede ser negativo o cero.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
                return;
            }
            TipoUsoPesa tipoUsoPesa = (TipoUsoPesa) selectTipoDeUsoPesa.getSelectedItem();
            SoporteTecnico st = controller.mostrarSoporteTecnico();
            // Crear objeto de tipo artículo con estos datos generales
            controller.crearTipoDePesa(st, pesoPesa, tipoUsoPesa, tipoDesgaste, desgasteMaximo, marcaArticulo, nombreArticulo, costoArticulo);
            JOptionPane.showMessageDialog(this, "Tipo de pesa creado con éxito");
            // A continuación, verificar cuál tipo de artículo se está creando (Pesa, Colchoneta, Accesorio)
            // y recolectar los datos específicos para cada uno.
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa un número válido para el costo.", "Error de formato", JOptionPane.ERROR_MESSAGE);
        } catch (Throwable e) {
            JOptionPane.showMessageDialog(this, "Error al crear el tipo de artículo: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearTipoPesaActionPerformed

