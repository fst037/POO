    private void btnCrearNuevoTipoClaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearNuevoTipoClaseActionPerformed
        try {
            String nombreTipoClase = txtNombreTipoClase.getText();
            List<TipoArticulo> articulosProfesor = new ArrayList<>();
            List<TipoArticulo> articulosCliente = new ArrayList<>();
            int grabacionesMax = (Integer) countGrabacionesMax.getValue();

            // Obtener los artículos del profesor
            DefaultListModel<TipoArticulo> modelProfesor = (DefaultListModel<TipoArticulo>) listArtProfesor.getModel();
            for (int i = 0; i < modelProfesor.getSize(); i++) {
                articulosProfesor.add(modelProfesor.getElementAt(i));
            }

            // Obtener los artículos del cliente
            DefaultListModel<TipoArticulo> modelCliente = (DefaultListModel<TipoArticulo>) listArtCliente.getModel();
            for (int i = 0; i < modelCliente.getSize(); i++) {
                articulosCliente.add(modelCliente.getElementAt(i));
            }

            // Llamar al método del controlador para crear el nuevo tipo de clase
            controller.crearTipoClase(nombreTipoClase, articulosProfesor, articulosCliente, grabacionesMax);

            if (grabacionesMax < 0) {
                JOptionPane.showMessageDialog(this, "El número de grabaciones máximas no puede ser negativo.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
                return; // Detener la ejecución del método si el número es negativo
            }

            // Mensaje de confirmación
            JOptionPane.showMessageDialog(this, "Tipo de clase creado con éxito");

            // Limpieza de formularios (opcional)
            txtNombreTipoClase.setText("");
            modelProfesor.clear();
            modelCliente.clear();

        } catch (Throwable e) {
            JOptionPane.showMessageDialog(this, "Error al crear el tipo de clase: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearNuevoTipoClaseActionPerformed

