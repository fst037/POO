    private void selectAdministrativoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectAdministrativoActionPerformed
        Administrativo adminSeleccionado = (Administrativo) selectAdministrativo.getSelectedItem();
        String nombreSede = txtFiltroNombreSedeAdmin.getText();

        if (adminSeleccionado != null && nombreSede != "") {
            refreshSelect(selectSedeAdministrativo, controller.listarSedesConFiltro(adminSeleccionado, nombreSede));
        } else if (adminSeleccionado != null && nombreSede == "") {
            refreshSelect(selectSedeAdministrativo, controller.listarSedesConFiltro(adminSeleccionado));
        }
    }//GEN-LAST:event_selectAdministrativoActionPerformed

