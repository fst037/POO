    private void configurarCampoFloat() {
        txtAlquiler.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char caracter = e.getKeyChar();
                if (((caracter < '0') || (caracter > '9')) && (caracter != KeyEvent.VK_BACK_SPACE) && (caracter != '.')) {
                    e.consume();  // Ignora el evento de tecla si no es un número, retroceso o punto.
                }
                if (caracter == '.' && txtAlquiler.getText().contains(".")) {
                    e.consume(); // Ignora el evento de tecla si ya hay un punto en el campo.
                }
            }
        });

        txtDNIAdministrativo.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char caracter = e.getKeyChar();
                if (((caracter < '0') || (caracter > '9')) && (caracter != KeyEvent.VK_BACK_SPACE)) {
                    e.consume();  // Ignora el evento de tecla si no es un número o retroceso
                }
                if (caracter == '.' && txtAlquiler.getText().contains(".")) {
                    e.consume(); // Ignora el evento de tecla si ya hay un punto en el campo.
                }
            }
        });
    }

